/** Чистая отображаемая категория по имени; при отсутствии совпадения — исходная. */
export function cleanDisplayCategory(name: string | null | undefined, rawCategory: string | null | undefined): string {
  const n = (name ?? '').toLowerCase().replace(/ё/g, 'е');
  // regex-таблица использует «ё» — нормализуем и её через тест по обеим формам исходного имени.
  const raw = (name ?? '').toLowerCase();
  for (const { re, category } of NAME_TO_CATEGORY) {
    if (re.test(n) || re.test(raw)) return category;
  }
  return (rawCategory ?? '').trim() || 'Прочее';
}
