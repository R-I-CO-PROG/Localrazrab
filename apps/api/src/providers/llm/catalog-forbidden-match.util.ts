// Грубый стеммер: срезаем типичные русские окончания мн.ч./падежей, чтобы «аккумуляторы»→«аккумулятор»,
// «колонки»→«колонк», «упаковки»→«упаковк».
function stem(word: string): string {
  return word.replace(/(ами|ями|иями|ов|ев|ый|ий|ая|яя|ое|ее|ые|ие|ыми|ими|ой|ей|ам|ям|ах|ях|ую|юю|у|ю|ы|и|а|я|е|о|ь)$/u, '');
}

// Слова-модификаторы/предлоги, которые сами по себе НЕ задают тип товара.
const STOPWORDS = new Set([
  'для', 'без', 'не', 'и', 'с', 'со', 'из', 'на', 'по', 'это', 'или', 'а',
  'беспроводные', 'беспроводной', 'беспроводная', 'беспроводный', 'беспроводн',
  'переносные', 'портативные', 'портативный', 'мини', 'разные', 'любые',
]);

// Термины-«ничего»: catch-all чипы, которые не должны ничего фильтровать.
const SKIP_TERMS = new Set(['другое', 'другой', 'прочее', 'прочие', 'other', 'разное', 'иное']);

function escapeRe(s: string): string {
  return s.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
}

/** Один паттерн запрета: rx + область матча (nameOnly — только по имени, без категории). */
export interface ForbiddenPattern {
  rx: RegExp;
  nameOnly: boolean;
}

/** Паттерны для ОДНОГО свободнотекстового запрета: семейства-синонимы + generic-стемы значимых слов. */
export function forbiddenTermPatterns(term: string): ForbiddenPattern[] {
  const t = norm(term);
  if (!t || SKIP_TERMS.has(t)) return [];
  const out: ForbiddenPattern[] = [];
  for (const fam of FORBIDDEN_FAMILIES) {
    if (fam.trigger.test(t)) out.push({ rx: fam.match, nameOnly: fam.nameOnly ?? false });
  }
  const stems = t
    .split(/[^a-zа-я0-9]+/i)
    .map((w) => stem(w))
    .filter((w) => w.length >= 5 && !STOPWORDS.has(w));
  for (const s of stems) out.push({ rx: new RegExp(escapeRe(s), 'i'), nameOnly: false });
  return out;
}

/** Текст товара для матчинга запретов: имя + описание + категория + подкатегория. */
function productHay(p: CatalogProduct): string {
  return norm(`${p.name} ${p.description ?? ''} ${p.category ?? ''} ${p.subcategory ?? ''}`);
}

/** Нарушает ли товар хотя бы один свободнотекстовый запрет пользователя. */
export function productMatchesForbidden(p: CatalogProduct, forbiddenItems: string[]): boolean {
  if (!forbiddenItems || !forbiddenItems.length) return false;
  const hayFull = productHay(p);
  const hayName = norm(p.name);
  for (const term of forbiddenItems) {
    // (1) Семьи-синонимы + грубые стемы значимых слов термина.
    for (const { rx, nameOnly } of forbiddenTermPatterns(term)) {
      if (rx.test(nameOnly ? hayName : hayFull)) return true;
    }
    // (2) Запрет ЦЕЛОЙ категории-бакета («зонты», «кружки», «свечи», «без электроники»):
    // короткие корни-категории стеммер (1) дропает (<5 симв) — матчим по правилам бакета.
    for (const bucket of bucketsForCategoryTerm(term)) {
      if (productMatchesAllowedBucket(p, bucket)) return true;
    }
  }
  return false;
}

/** Отфильтровать каталог от свободнотекстовых запретов (ХАРД — вызывающий решает про размер пула). */
export function filterOutForbidden(
  catalog: CatalogProduct[],
  forbiddenItems: string[],
): CatalogProduct[] {
  if (!forbiddenItems || !forbiddenItems.length) return catalog;
  return catalog.filter((p) => !productMatchesForbidden(p, forbiddenItems));
}
