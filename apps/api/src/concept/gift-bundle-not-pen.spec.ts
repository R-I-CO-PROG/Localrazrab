import { describe, it, beforeEach } from 'node:test';
import assert from 'node:assert/strict';
import type { CatalogProduct } from '../providers/llm/catalog.util';
import { clearProductTypeCache, detectTypeSlug, familyForType } from './product-taxonomy';
import { isGiftBundleProduct, isPackagingProduct } from './product-role.util';

/**
 * Готовый подарочный набор («ежедневник + ручка в коробке») — это БАНДЛ, а не ручка.
 *
 * Ветка «пишущий набор» в detectPrimaryTypeFromName отсеивала многосоставные наборы
 * ЧЁРНЫМ списком вложений (плед|термос|кружка|сумка|часы…). Список fail-open: «ежедневник»,
 * «блокнот», «аккумулятор», «флешка», «маска» в него не входили — и «Подарочный набор Рейн
 * (ежедневник, ручка)» становился `pen`. Последствия в подборе:
 *   1) бандл занимал роль «ручка» → обязательная ручка считалась выполненной;
 *   2) он не опознавался как giftBundle → готовый набор клали ВНУТРЬ собираемого набора;
 *   3) к нему добирали ещё ежедневник — набор из трёх письменных предметов.
 * На боевом каталоге так ломались 111 из 1285 подарочных наборов (8.6%).
 *
 * Правило теперь позитивное: набор считается пишущим, только если в названии не упомянут
 * ни один другой предметный тип.
 */

let seq = 0;
function mk(name: string, category = 'Подарочные наборы'): CatalogProduct {
  return { id: `t${seq++}`, name, category } as CatalogProduct;
}

describe('готовый подарочный набор не должен опознаваться как ручка', () => {
  beforeEach(() => clearProductTypeCache());

  // Реальные названия из боевого каталога.
  const bundles = [
    'Подарочный набор Рейн (Rain), черный (ежедневник, ручка)',
    'Подарочный набор Виста (Vista), черный/красный (ежедневник, ручка, аккумулятор)',
    'Подарочный набор Vision Pro soft-touch с ручкой и блокнотом А5, оранжевый',
    'Подарочный набор "Tesla" с блокнотом А5, ручкой и пауэрбенком на 10 000 mAh, черный',
    'Подарочный набор Tactical Dark: блокнот А5, ручка шариковая',
    'Подарочный набор Эрба НЕО (Erba NEO), красный (шоппер, блокнот, ручка)',
  ];

  for (const name of bundles) {
    it(`бандл → gift_set, а не pen: ${name.slice(0, 46)}`, () => {
      const slug = detectTypeSlug(mk(name));
      assert.notEqual(slug, 'pen', `«${name}» опознан как ручка`);
      assert.equal(familyForType(slug), 'bundle', `семейство должно быть bundle, а не ${familyForType(slug)}`);
      assert.ok(isGiftBundleProduct(mk(name)), 'должен считаться готовым подарочным набором');
    });
  }

  it('многосоставный набор без слова «подарочный» тоже не ручка', () => {
    assert.notEqual(detectTypeSlug(mk('Набор ручка + флеш-карта 16Гб в футляре, красный')), 'pen');
  });

  // Граница метода: «непишущий предмет» распознаётся через SLUG_RULES. Для маски и кухонной
  // утвари правил нет, поэтому такие наборы всё ещё считаются ручкой. Чинится добавлением
  // правила типа, а не расширением этой ветки.
  it('ИЗВЕСТНОЕ ОГРАНИЧЕНИЕ: предмет без правила типа не опознаётся', { todo: true }, () => {
    assert.notEqual(detectTypeSlug(mk('Набор: Защитная маска BYRON + Шариковая авторучка')), 'pen');
  });

  // Отдельный дефект: «ручки» здесь — рукоятки, а не пишущие принадлежности.
  it('ИЗВЕСТНОЕ ОГРАНИЧЕНИЕ: «со стальными ручками» — это рукоятки', { todo: true }, () => {
    assert.notEqual(detectTypeSlug(mk('Набор кухонных предметов со стальными ручками «Paul Bocuse»')), 'pen');
  });

  it('«Блокнот с ручкой и набором стикеров» — это блокнот', () => {
    assert.equal(detectTypeSlug(mk('Блокнот с ручкой и набором стикеров А5 "Write and stick", серый')), 'notebook');
  });
});

describe('настоящие наборы пишущих принадлежностей остаются ручками', () => {
  beforeEach(() => clearProductTypeCache());

  const writingSets = [
    'Подарочный набор ручек "Dumas": ручка шариковая и роллер, 4 стержня, футляр',
    'Подарочный набор из 2-х ручек Parker URBAN Muted black GT перьевая и шариковая ручки',
    'Rivulet Подарочный набор из двух ручек',
    'Набор REYDON: ручка роллер, карандаш механический',
    'Набор Geneva, включающий шариковую ручку-стилус и ручку-роллер',
    'Набор AMAZONIA из 6 карандашей в коробке',
  ];

  for (const name of writingSets) {
    it(`пишущий набор → pen: ${name.slice(0, 46)}`, () => {
      const slug = detectTypeSlug(mk(name, 'Канцелярские товары'));
      assert.equal(familyForType(slug), 'pen', `«${name}» → ${slug} (ожидали семейство pen)`);
    });
  }

  // Регрессия состязательного ревью: `cleaning_cloth` — филлер (family `packaging`), а не предмет
  // набора. Пока он считался «непишущим предметом», набор с салфеткой становился упаковкой и
  // вычищался пост-гейтами — это было хуже прежнего поведения.
  it('салфетка-филлер не лишает набор роли ручки', () => {
    const p = mk('Набор: ручка металлическая и салфетка из микрофибры', 'Канцелярские товары');
    const slug = detectTypeSlug(p);
    assert.equal(familyForType(slug), 'pen', `«набор с салфеткой» → ${slug}`);
    assert.ok(!isPackagingProduct(p), 'набор с ручкой не должен считаться упаковкой');
  });

  it('готовый бандл-обёртка не считается предметом-вложением', () => {
    // welcome_pack / gift_set сами по себе бандлы — они не должны «блокировать» роль pen.
    const slug = detectTypeSlug(mk('Набор ручек в подарочной коробке', 'Канцелярские товары'));
    assert.equal(familyForType(slug), 'pen', `«набор ручек в коробке» → ${slug}`);
  });
});
